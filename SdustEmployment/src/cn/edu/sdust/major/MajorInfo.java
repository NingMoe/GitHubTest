package cn.edu.sdust.major;

public class MajorInfo {          //专业信息
	
	private String majorName;
	private String majorReleaseTime;
	private String majorIntro;
	private String majorId;
	
	public String getMajorName() {
		return majorName;
	}
	public void setMajorName(String majorName) {
		this.majorName = majorName;
	}
	public String getMajorReleaseTime() {
		return majorReleaseTime;
	}
	public void setMajorReleaseTime(String majorReleaseTime) {
		this.majorReleaseTime = majorReleaseTime;
	}
	public String getMajorIntro() {
		return majorIntro;
	}
	public void setMajorIntro(String majorIntro) {
		this.majorIntro = majorIntro;
	}
	public String getMajorId() {
		return majorId;
	}
	public void setMajorId(String majorId) {
		this.majorId = majorId;
	}
	

}
