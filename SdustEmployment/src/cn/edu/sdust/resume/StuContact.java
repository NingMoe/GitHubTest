package cn.edu.sdust.resume;

public class StuContact {
	private String studentTel;
	private String studentEmail;
	private String studentQQ;
	private String studentZip;
	private String studentAddress;
	public String getStudentTel() {
		return studentTel;
	}
	public void setStudentTel(String studentTel) {
		this.studentTel = studentTel;
	}
	public String getStudentEmail() {
		return studentEmail;
	}
	public void setStudentEmail(String studentEmail) {
		this.studentEmail = studentEmail;
	}
	public String getStudentQQ() {
		return studentQQ;
	}
	public void setStudentQQ(String studentQQ) {
		this.studentQQ = studentQQ;
	}
	public String getStudentZip() {
		return studentZip;
	}
	public void setStudentZip(String studentZip) {
		this.studentZip = studentZip;
	}
	public String getStudentAddress() {
		return studentAddress;
	}
	public void setStudentAddress(String studentAddress) {
		this.studentAddress = studentAddress;
	}
}
